# Declaración en inicialización de un diccionario
diccionario = {"Alemania" : "Berlín", "Francia" : "París", "Reino Unido" : "London", "España" : "Madrid"}

# Imprimir un valor del diccionario
print(diccionario["Francia"]) 

# Imprime todo el diccionario con clave y valor
print(diccionario)

# Agregar elementos
diccionario["Italia"] = "Lisboa"
print(diccionario)

# Modificar elementos
diccionario["Italia"] = "Roma"
print(diccionario)

#Eliminar elementos
del diccionario["Reino Unido"]
print(diccionario)